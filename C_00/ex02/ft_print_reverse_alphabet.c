#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_print_alphabet(void)
{
	char symbol;

	symbol = 'z';
	while (symbol >= 'a')
	{
		ft_putchar(symbol);
		symbol--;
	}
}
